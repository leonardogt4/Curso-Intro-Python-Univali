print("Teste IF/ELSE")

nota1 = input("Entre sua nota:")
nota1 = float(nota1)

if nota1 >= 9:
    print("Sua nota é A")
    print("Parabéns")
elif nota1 >= 8:
    print("Sua nota é B")
    print("Bom")
elif nota1 >= 7:
    print("Sua nota é C")
    print("Médio")
elif nota1 >= 6:
    print("Sua nota é D")
    print("Ruim")
else:
    print("Sua nota é E")
    print("Pereceu!")

print("End")
    
